
function showDogs() {
    var dogs = document.getElementById("resultcontainer");
    var cats = document.getElementById("resultcontainercat");
    var rabbits = document.getElementById("resultcontainerrabbit");
    var Others = document.getElementById("resultcontainerothers");

    dogs.style.display = "block";
    cats.style.display = "none";
    rabbits.style.display = "none";
    Others.style.display = "none";

}
